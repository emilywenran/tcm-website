export const solarTerms: SolarTerm[] = Object.values(dates)
  .flat()
  .map(([name, date]) => ({ name, date, hint: hints[name] }));

export function getCurrentSolarTerm(date = new Date()): SolarTerm {
  const time = new Date(date.getFullYear(), date.getMonth(), date.getDate()).getTime();
  const sameYear = solarTerms.filter((term) => new Date(term.date).getFullYear() === date.getFullYear());
  const terms = sameYear.length ? sameYear : solarTerms;
  return terms.reduce((current, term) => {
    const termTime = new Date(`${term.date}T00:00:00`).getTime();
    return termTime <= time ? term : current;
  }, terms[0]);
}
